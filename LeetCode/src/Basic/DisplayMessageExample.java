// DisplayMessageExample.java
// This program demonstrates how to display a message to the console in Java.

public class DisplayMessageExample {
    public static void main(String[] args) {
        // Define a message to display
        String message = "Hello, welcome to the Java programming world!";
        
        // Display the message to the console
        System.out.println(message);
    }
}

// Example Output:
// Hello, welcome to the Java programming world!